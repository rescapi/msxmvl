;--- Open file ---

open_file:	ld	de,upper_fcb
	ld	c,open
	call	bdoscall
	ld	hl,1
	ld	(upper_fcb + 0Eh),hl
	dec	hl
	ld	(upper_fcb + 021h),hl
	ld	(upper_fcb + 023h),hl
	ret

;--- Load (part of) file ---
; In: HL = number of bytes to read
;     DE = transfer address

load_file:	push	ix
	push	hl
	push	de
	push	hl
	ld	c,setdma
	call	bdoscall
	ld	de,upper_fcb
	pop	hl
	ld	c,read
	call	bdoscall
	pop	de
	pop	hl
	pop	ix
	ret


;--- Close file ---

close_file:	ld	de,upper_fcb
	ld	c,close
	call	bdoscall
	ret


