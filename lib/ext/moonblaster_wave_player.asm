;--- pitch bending ---

play_pitch_wdo: exx

	ld	c,a

	ld	l,(iy + 3)	; pitch bend speed
	ld	h,(iy + 4)
	dec	c
	jp	nz,play_mod_wdo	; modulation
	ex	de,hl
play_pitch_wd4: ld	h,(iy + 19)
	ld	l,(iy + 18)
	add	hl,de	; sliding
	bit	3,h
	jr	z,play_pitch_wd5
	bit	7,d
	jr	nz,play_pitch_wd6
	ld	a,h
	add	a,1000b
	ld	h,a
	jr	play_pitch_wd5
play_pitch_wd6: res	3,h
play_pitch_wd5: ld	(iy + 18),l	 ; freq fine
	ld	(iy + 19),h

	ld	a,(iy + 1)
	ld	c,a
	out	(WVIO),a
	ld	a,l
	or	(iy + 6)
	; opl4_wait (empty: Z80 build)
	out	(WVIO+1),a	; freq + tone

	ld	a,c
	add	a,24
	; opl4_wait (empty: Z80 build)
	out	(WVIO),a
	ld	a,h
	; opl4_wait (empty: Z80 build)
	out	(WVIO+1),a	; freq
	exx
	jp	(hl)


