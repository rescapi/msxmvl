;--- Init mapper ---
; In: -
; Out: Checks DOS version and inits mapper routines.
;      Also 'allocates' first 64K

InitMapper:	ld	a,255
	ld	(DOSinit),a
	xor	a
	ld	c,06Fh
	call	bdoscall
	ld	a,b
	ld	(DOSversion),a
	cp	2
	jr	c,InitMapperDOS1

	xor	a	; get mapper variable table
	ld	d,4
	ld	e,1
	call	0FFCAh
	ld	(DOSVarTab),hl
	inc	hl
	inc	hl
	ld	a,(hl)
	ld	(freeDOS2segs),a

	xor	a	; get mapper jump table
	ld	d,4
	ld	e,2
	call	0FFCAh
	ld	(DOSJmpTab),hl

	ld	de,1bh	; Fill 1st 4 fields of segtable
	call	dos2_segrtn
	ld	(DOSSegTab + 3),a
	ld	de,21h
	call	dos2_segrtn
	ld	(DOSSegTab + 2),a
	ld	de,27h
	call	dos2_segrtn
	ld	(DOSSegTab + 1),a
	ld	de,2dh
	call	dos2_segrtn
	ld	(DOSSegTab + 0),a
	ret

;- allocate in DOS1 -

InitMapperDOS1: ld	hl,0203h
	ld	(DOSSegTab),hl
	ld	hl,0001h
	ld	(DOSSegTab + 2),hl
	ret


; --- Select segment/bank on 08000h-0BFFFh
; In: A = seg/banknr

selbank_FE:	push	bc
	ld	b,a
	ld	a,(DOSversion)
	cp	2
	jr	c,Selbank_FE2	; DOS1
	push	hl
	push	de
	ld	a,b
	ld	hl,DOSSegTab
	add	a,l
	jr	nc,__ahl_14
	inc	h
__ahl_14:	ld	l,a
	ld	a,(hl)	; zoek segmentnr
	ld	de,24h
	call	dos2_segrtn
	pop	de
	pop	hl
	pop	bc
	ret
Selbank_FE2:	ld	a,b
	ld	(DOSSegTab + 2),a
	out	(0FEh),a
	pop	bc
	ret

;--- Calls DOS2 mapper routine ---
; In: DE = routine offset

dos2_segrtn:	ld	hl,(DOSJmpTab)
	add	hl,de
	push	hl
	ret


; Get current segment/bank on 08000h-0BFFFh
; Out: A = seg/banknr

curbank_FE:	ld	a,(DOSversion)
	cp	2
	jr	c,curbank_FE2		; DOS1
	ld	de,27h
	call	dos2_segrtn
	ld	hl,DOSSegTab
	ld	d,0
curbank_FE3:	cp	(hl)
	jr	z,curbank_FE4
	inc	hl
	inc	d
	jp	curbank_FE3
curbank_FE4:	ld	a,d
	ret
curbank_FE2:	ld	a,(DOSSegTab + 2)
	ret


;--- Allocate segments ---
; In: A = nr of segments

AllocSegs:	ld	b,a
	ld	a,(DOSNrSegs)
	or	a
	ret	nz
	ld	a,b
	or	a
	ret	z	; 0 segments? return
	ld	(DOSNrSegs),a
	ld	hl,DOSSegTab + 4
	ld	c,0
	ld	b,a
alloc_seglp:	push	bc
	push	hl
	ld	a,1	; system segment
	ld	b,0	; primary mapper
	ld	de,0
	call	dos2_segrtn	; call alloc routine
	pop	hl
	ld	(hl),a
	inc	hl
	pop	bc
	inc	c
	djnz	alloc_seglp
	ret		; B = 0

; Free segments

FreeSegs:	ld	a,(DOSversion)
	cp	2
	jr	c,free_seg2
	ld	a,(DOSNrSegs)
	ld	b,a
	ld	hl,DOSSegTab + 4
	ld	b,a
free_segl:	push	bc
	push	hl
	ld	a,(hl)
	ld	b,0
	ld	de,3
	call	dos2_segrtn
	pop	hl
	inc	hl
	pop	bc
	djnz	free_segl
free_seg2:	xor	a
	ld	(DOSNrSegs),a
	ret


DOSinit:	db	0
DOSversion:	db	0
DOSVarTab:	dw	0
DOSJmpTab:	dw	0
DOSSegTab:	ds	256,0
DOSNrSegs:	db	0


