;-------------------------------- PLAYROUTINE --------------------------------

; start_music = start music
; stop_music = stop music
; cont_music = continues music after pause
; halt_music = halts/pauses music

play_busy:	equ	0da00h	; status:   0 = not playing
			;	 255 = playing
songdata_bank1: equ	0da01h	; mapperbank with song data
songdata_bank2: equ	0da02h	; mapperbank with song data
songdata_bank3: equ	0da03h	; mapperbank with song data
songdata_adres: equ	0da04h	; address of song data
play_pos:	equ	0da06h	; current position
play_step:	equ	0da07h	; current step
status:	equ	0da08h	; status bytes (0 = off)
step_buffer:	equ	0da0bh	; decrunched step, played next int

load_buffer:	equ	0da23h
freeDOS2segs:	equ	0da26h
upper_fcb:	equ	0da40h

;--------------------
;--- Start music ---
;--------------------
; In : -
; Out: -
; Mod: all

start_music:	di
	ld	a,(play_busy)
	or	a
	ret	nz	; already playing?

	ld	hl,0
	ld	(status),hl
	ld	(status+1),hl	; clear status bytes

	ld	a,0ffh
	ld	(play_busy),a	; set busy playing
	xor	a
	ld	(play_hzeql),a	; reset hz equalizer counter

	ld	a,15
	ld	(play_step),a
	ld	a,255
	ld	(play_pos),a	; set position and step

	call	curbank_FE
	push	af
	ld	a,(songdata_bank1)
	call	selbank_FE

	ld	hl,(songdata_adres)
	ld	de,xleng
	ld	bc,220
	ldir		; copy song settings
	ld	de,58
	add	hl,de	; skip name/wavekit
	ld	(pos_address),hl
	ld	a,(xleng)
	inc	a
	ld	e,a
	add	hl,de
	ld	(pat_address),hl
	pop	af
	call	selbank_FE

	call	init_opl4	; initialise OPL4
	call	init_voices	; set start voices
	ld	a,(xtempo)
	ld	(play_speed),a	; set tempo
	sub	3
	ld	(play_timercnt),a ; initialise timer (tempo)
	xor	a
	ld	(play_tspval),a	; transpose off
	IF	FADE
	ld	(play_fading),a
	ld	(play_fadecnt),a
	ld	(play_fadetcnt),a
	ENDIF

start_mus_cnt:	di
	ld	hl,0fd9fh
	ld	de,old_int
	ld	bc,5
	ldir		; save interrupt hook

	ld	a,(0f342h)
	ld	(play_int_han+1),a
	ld	hl,play_int_han
	ld	de,0fd9fh
	ld	bc,5
	ldir		; set music interrupt hook
	ei
	ret

play_int_han:	rst	030h	; interslot call to music int.
	db	0
	dw	play_int
                ret

;--- initialise OPL4 registers ---

init_opl4:	ld	a,3
	ld	c,5
	call	opl4_out_bnk1	; Set the OPL4 in OPL4 mode

	ld	c,2
	ld	a,10000b
	jp	opl4_out_wave	; init Wave ROM stuff



;----------------------------
;--- Stel Start Voices in ---
;----------------------------

init_voices:	ld	b,WAVCHNS	; # wave channels
	ld	iy,play_table_wav
	ld	ix,xbegwav
	ld	de,PTW_SIZE
init_wavesl:	push	de
	push	bc
	ld	a,(ix - 72)	; xdetune!
	add	a,a
	ld	(iy + 5),a	; detune!

	ld	a,(ix + 0)	; wave/patchnr
	push	af
	call	play_wwavevt2
	pop	af
	ld	hl,xwavvols - 1
	add	a,l
	jr	nc,__ahl_1
	inc	h
__ahl_1:	ld	l,a
	ld	a,(hl)	; volume
	ld	(iy + 15),a
	call	play_wchgvol2
	ld	a,(ix - 98)      ; stereo preset
	call	play_wchgste2
	inc	ix
	pop	bc
	pop	de
	add	iy,de
	djnz	init_wavesl
	ret



;-----------------------
;--- Continue muziek ---
;-----------------------
; In : -
; Out: -
; Mod: all

cont_music:	ld	a,(play_busy)	; already playing?
	or	a
	ret	nz
	dec	a
	ld	(play_busy),a
	jp	start_mus_cnt


;--------------------
;--- Stop muziek ----
;--------------------
; In : -
; Out: -
; Mod: all

stop_music:	jr	halt_music

;------------------
;--- Halt music ---
;------------------

halt_music:	ld	a,(play_busy)	; already stopped?
	or	a
	ret	z
	di
	xor	a
	ld	(play_busy),a
	ld	hl,old_int	; restore old interrupt hook
	ld	de,0fd9fh
	ld	bc,5
	ldir

	ld	b,WAVCHNS	; # wave channels
	ld	iy,play_table_wav
	ld	de,PTW_SIZE
halt_musicl3:	call	play_woffevt
	call	play_chgdmp
	add	iy,de
	djnz	halt_musicl3
	ei
	ret

;------------------
;--- Fade music ---
;------------------
; In : A is fade speed
; Out: -
; Mod: AF, B

	IF	FADE

fade_music:	ld	b,a
	ld	a,(play_busy)	; already stopped?
	or	a
	ret	z
	ld	a,255
	ld	(play_fading),a	; fading on
	ld	a,b
	ld	(play_fadespd),a	; set fade speed
	ret

	ENDIF

