;--- Load MWM file ---

; Note: The routine below is a bit complex because it supports
; songs > 16K. However, if you know that your song will always be < 16K you
; can simplify it a lot:
; - read the header and trash it!
; - read the rest of the file
; - modify the play_nextpos routine so that 3 is added to the pattern address

mbload:	ld	a,(DOSinit)
	or	a
	jp	z,initerror
	ld	ix,04c64h
	call	basic_call
	push	hl
	ld	ix,067d0h		; get filename
	call	basic_call
	push	hl
	pop	ix
	call	build_fcb		; build right FCB

	call	curbank_FE
	push	af

	call	open_file		; open song file
	or	a
	jp	nz,loderr

	ld	hl,songdata_bank1 	; select first song bank
	ld	(load_bank),hl
	ld	a,(hl)
	call	selbank_FE

	ld	hl,6
	ld	de,(songdata_adres)
	call	load_file		; read header
	ld	a,8		; file type 8 = wave user song
	call	check_header
	jr	nz,loderr2

	ld	hl,278
	ld	de,(songdata_adres)	; read settings
	call	load_file
	ld	a,(de)
	add	hl,de
	ex	de,hl
	inc	a
	ld	l,a
	ld	h,0
	call	load_file		; read positions
	call	check_pats
	add	hl,de
	ex	de,hl
	add	a,a
	ld	l,a
	ld	h,0
	call	load_file		; read pattern addresses
	add	hl,de
	ld	(load_adres),hl

mbload_lp:	ld	de,load_buffer
	ld	hl,3
	call	load_file
	ld	a,(load_buffer + 2)
	or	a
	jr	z,mbload2
	ld	de,(load_adres)
	ld	hl,(load_buffer)
	call	load_file
	ld	de,08000h
	ld	(load_adres),de
	ld	hl,(load_bank)
	inc	hl
	ld	a,(hl)
	ld	(load_bank),hl
	call	selbank_FE
	jr	mbload_lp

mbload2:	call	close_file
	pop	af
	call	selbank_FE

	call	stop_drive
	pop	hl
	ret

;--- load error ---

